package TestPackage;

public class FinalConcept {

	public static void main(String[] args) {

		// Final keyword is used to define constant values.
		// we cannot modify the value of final variable
		// final keyword is used to prevent the inheritance.

		final int i = 10;
		/*
		 * i = 20; i = 30;
		 */

		System.out.println(i);

	}

}
